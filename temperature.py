# latihan temperature

celcius = float(input("Masukkan celcius :"))

# celcius

print("temperatur adalah :", celcius, "celcius")

# farenheit

farenheit = (9/5) * celcius + 32
print("temperatur adalah :", farenheit, "celcius")

# reumur

reumur = (4/5) * celcius
print("temperatur adalah :", reumur, "celcius")

# kelvin

kelvin = 273 + celcius
print("temperatur saat ini adalah :", kelvin, "celcius")